use std::collections::HashMap;

#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct ConversationMessage {
    pub role: String,
    pub content: String,
}

#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct AgentInput {
    /// 节点ID
    pub node_id: String,
    /// 任务描述（来自 Planner）
    pub task: String,
    /// 来自上游依赖的输出（key = dependency node ID）
    #[serde(default)]
    pub dependencies: HashMap<String, AgentOutput>,
    /// 节点特定的 JSON 负载负载（可选）
    #[serde(default)]
    pub schema_payload: Option<String>,
    /// 短期工作记忆：多轮原生的用户与助手对话历史 (取代之前拼接大字符串的方式)
    #[serde(default)]
    pub conversation_history: Vec<ConversationMessage>,
    /// 注入的长期记忆事实文本（可选）：仅用于存放 UserProfile 和 Episodic Summaries
    #[serde(default)]
    pub memory_context: Option<String>,
    /// Corrective feedback from the Critic node in a loop iteration.
    /// When present, the Actor should use this to adjust its behavior
    /// rather than blindly repeating the same operation.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub correction: Option<CorrectionDirective>,
}

