#[derive(Debug, Clone, PartialEq, serde::Serialize, serde::Deserialize)]
pub struct SubGraphNode {
    pub id: String,
    pub agent_type: String, // e.g., "coder", "reviewer", "tester"
    pub task: String,
    #[serde(default)]
    pub schema_payload: String, // specific JSON payload the worker expects
    /// If set, this node participates in a corrective loop (Actor-Critic pattern)
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub loop_config: Option<LoopConfig>,
    /// True if this node acts as the Critic in a loop (its output drives CorrectionDirective)
    #[serde(default)]
    pub is_critic: bool,
}

#[derive(Debug, Clone, PartialEq, serde::Serialize, serde::Deserialize)]
pub struct SubGraphEdge {
    pub from: String,
    pub to: String,
    pub dep_type: DependencyType,
}

#[derive(Debug, Clone, PartialEq, serde::Serialize, serde::Deserialize)]
pub struct AgentSubGraph {
    pub nodes: Vec<SubGraphNode>,
    pub edges: Vec<SubGraphEdge>,
}

