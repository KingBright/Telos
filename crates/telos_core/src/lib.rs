/// Configuration for a corrective loop node
#[derive(Debug, Clone, PartialEq, serde::Serialize, serde::Deserialize)]
pub struct LoopConfig {
    /// Hard upper bound on iteration count (circuit breaker)
    pub max_iterations: usize,
    /// When this condition is met, exit the loop
    pub exit_condition: ExitCondition,
    /// ID of the paired Critic node (whose output drives correction)
    pub critic_node_id: String,
}

/// Condition that terminates the loop
#[derive(Debug, Clone, PartialEq, serde::Serialize, serde::Deserialize)]
pub enum ExitCondition {
    /// Exit when Critic's satisfaction_score >= threshold
    SatisfactionThreshold(f32),
    /// Exit when Critic output contains a specific JSON key
    OutputContains(String),
}

/// Correction directive generated by the Critic node, injected into the next Actor iteration.
/// This is the KEY mechanism preventing blind retries — each iteration carries
/// specific diagnostic feedback and corrective instructions.
#[derive(Debug, Clone, serde::Serialize, serde::Deserialize)]
pub struct CorrectionDirective {
    /// Current iteration number (1-indexed)
    pub iteration: usize,
    /// Quality score from previous iteration (0.0 ~ 1.0)
    pub satisfaction_score: f32,
    /// Diagnosis of what went wrong in the previous iteration
    pub diagnosis: String,
    /// Specific corrective instructions (e.g., "search for X instead of Y")
    pub correction_instructions: Vec<String>,
    /// Summary of previous iteration's output (to avoid repeating the same work)
    pub previous_summary: String,
}

